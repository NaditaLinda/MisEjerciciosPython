# Definición de la función con el parámetro 'nombre'
def saludar_personalizado(nombre):
    """
    Muestra un saludo que incluye el nombre pasado como argumento.
    """
    print(f"¡hola {nombre}!")

# Llamada a la función pasando diferentes cadenas
saludar_personalizado("Carlos")
saludar_personalizado("Marta")